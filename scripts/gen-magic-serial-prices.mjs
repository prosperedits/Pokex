#!/usr/bin/env node
// Bundle TCGplayer prices for Magic SERIALIZED cards that Scryfall leaves
// unpriced. Scryfall (our live Magic price source) doesn't track most serialized
// cards — they're too rare to have market data — but TCGplayer does. tcgcsv.com
// (the free TCGplayer mirror) blocks browser CORS, so we can't fetch it live;
// instead we bundle the gap-fills here, keyed by EXACT tcgplayer_id (which equals
// the TCGCSV productId, so there's no fuzzy name matching). Re-run to refresh.
//
//   node scripts/gen-magic-serial-prices.mjs   →  data/magic-prices.js
import { writeFileSync } from 'node:fs';

// Mirror the Magic codes/names in app.js GAME_SETS.
const SETS = [
  { code: 'tdm', name: 'Tarkir: Dragonstorm' }, { code: 'fin', name: 'Final Fantasy' },
  { code: 'eoe', name: 'Edge of Eternities' }, { code: 'tla', name: 'Avatar: The Last Airbender' },
  { code: 'spm', name: "Marvel's Spider-Man" }, { code: 'dsk', name: 'Duskmourn: House of Horror' },
  { code: 'blb', name: 'Bloomburrow' }, { code: 'mh3', name: 'Modern Horizons 3' },
  { code: 'otj', name: 'Outlaws of Thunder Junction' }, { code: 'mkm', name: 'Murders at Karlov Manor' },
];
const sleep = (ms) => new Promise((r) => setTimeout(r, ms));
const getJSON = async (url) => {
  const r = await fetch(url, { headers: { 'User-Agent': 'pokex/1.0', Accept: 'application/json' } });
  if (!r.ok) throw new Error(`${r.status} ${url}`);
  return r.json();
};

// 1) TCGCSV Magic (category 1) groups → groupId by abbreviation and by name
const groups = (await getJSON('https://tcgcsv.com/tcgplayer/1/groups')).results;
const byAbbr = new Map(groups.map((g) => [String(g.abbreviation || '').toUpperCase(), g.groupId]));
const byName = new Map(groups.map((g) => [g.name.toLowerCase(), g.groupId]));

const OUT = {};
for (const { code, name } of SETS) {
  // 2) Scryfall serialized cards for this set → their tcgplayer_ids
  let serial = [];
  try {
    let url = `https://api.scryfall.com/cards/search?q=set%3A${code}+is%3Aserialized&unique=prints`;
    while (url) { const j = await getJSON(url); serial.push(...(j.data || [])); url = j.has_more ? j.next_page : null; await sleep(120); }
  } catch { /* 404 = no serialized cards in this set */ }
  if (!serial.length) { console.log(`${code}: no serialized cards`); continue; }
  // 3) TCGCSV group for this set → its price feed by productId
  const gid = byAbbr.get(code.toUpperCase()) || byName.get(name.toLowerCase());
  if (!gid) { console.log(`${code}: no TCGCSV group match`); continue; }
  const prices = (await getJSON(`https://tcgcsv.com/tcgplayer/1/${gid}/prices`)).results;
  const priceById = new Map(prices.map((p) => [p.productId, p.marketPrice ?? p.midPrice ?? p.lowPrice]));
  let n = 0;
  for (const c of serial) {
    const tid = c.tcgplayer_id, mkt = tid != null ? priceById.get(tid) : null;
    if (tid != null && mkt) { OUT[tid] = +(+mkt).toFixed(2); n++; }
  }
  console.log(`${code}: ${serial.length} serialized, ${n} priced via TCGplayer`);
  await sleep(150);
}

writeFileSync(new URL('../data/magic-prices.js', import.meta.url),
  '// Auto-generated by scripts/gen-magic-serial-prices.mjs — do NOT edit by hand.\n' +
  '// TCGplayer market prices for Magic SERIALIZED cards Scryfall leaves unpriced,\n' +
  '// keyed by tcgplayer_id. Re-run the script to refresh.\n' +
  `window.MAGIC_PRICES = ${JSON.stringify(OUT)};\n`);
console.log(`\nwrote data/magic-prices.js with ${Object.keys(OUT).length} prices`);
